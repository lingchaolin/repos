package model;

public class Card_info_record {
	private Integer recordId;
	private Integer cardId;
	private Double infoType;
	private String createTime;
	private Integer userId;
	private String remark;

	public Card_info_record(Integer recordId, Integer cardId, Double infoType, String createTime, Integer userId,
			String remark) {
		super();
		this.recordId = recordId;
		this.cardId = cardId;
		this.infoType = infoType;
		this.createTime = createTime;
		this.userId = userId;
		this.remark = remark;
	}

	public Card_info_record() {
		super();
	}

	public Integer getRecordId() {
		return recordId;
	}

	public void setRecordId(Integer recordId) {
		this.recordId = recordId;
	}

	public Integer getCardId() {
		return cardId;
	}

	public void setCardId(Integer cardId) {
		this.cardId = cardId;
	}

	public Double getInfoType() {
		return infoType;
	}

	public void setInfoType(Double infoType) {
		this.infoType = infoType;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public Integer getUserId() {
		return userId;
	}

	public void setUserId(Integer userId) {
		this.userId = userId;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

}
