package model;

public class Card {
	private Integer cardId;
	private String cardCode;
	private Integer cardType;
	private String name;
	private String idcard;
	private String sex;
	private String telphone;
	private String mobile;
	private String email;
	private String address;
	private String work;
	private String zxbj;
	private Double monthlyMoney;
	private Double frozenMoney;
	private Double walletMoney;
	private Integer status;

	private String turn;
	private String change;

	public Card() {
		super();
	}

	public Card(Integer cardId, String cardCode, Integer cardType, String name, String idcard, String sex,
			String telphone, String mobile, String email, String address, String work, String zxbj, Double monthlyMoney,
			Double frozenMoney, Double walletMoney, Integer status) {
		super();
		this.cardId = cardId;
		this.cardCode = cardCode;
		this.cardType = cardType;
		this.name = name;
		this.idcard = idcard;
		this.sex = sex;
		this.telphone = telphone;
		this.mobile = mobile;
		this.email = email;
		this.address = address;
		this.work = work;
		this.zxbj = zxbj;
		this.monthlyMoney = monthlyMoney;
		this.frozenMoney = frozenMoney;
		this.walletMoney = walletMoney;
		this.status = status;
	}

	public Integer getCardId() {
		return cardId;
	}

	public void setCardId(Integer cardId) {
		this.cardId = cardId;
	}

	public String getCardCode() {
		return cardCode;
	}

	public void setCardCode(String cardCode) {
		this.cardCode = cardCode;
	}

	public Integer getCardType() {
		return cardType;
	}

	public void setCardType(Integer cardType) {
		this.cardType = cardType;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getIdcard() {
		return idcard;
	}

	public void setIdcard(String idcard) {
		this.idcard = idcard;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public String getTelphone() {
		return telphone;
	}

	public void setTelphone(String telphone) {
		this.telphone = telphone;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getWork() {
		return work;
	}

	public void setWork(String work) {
		this.work = work;
	}

	public String getZxbj() {
		return zxbj;
	}

	public void setZxbj(String zxbj) {
		this.zxbj = zxbj;
	}

	public Double getMonthlyMoney() {
		return monthlyMoney;
	}

	public void setMonthlyMoney(Double monthlyMoney) {
		this.monthlyMoney = monthlyMoney;
	}

	public Double getFrozenMoney() {
		return frozenMoney;
	}

	public void setFrozenMoney(Double frozenMoney) {
		this.frozenMoney = frozenMoney;
	}

	public Double getWalletMoney() {
		return walletMoney;
	}

	public void setWalletMoney(Double walletMoney) {
		this.walletMoney = walletMoney;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public String getTurn() {
		return turn;
	}

	public void setTurn(String turn) {
		this.turn = turn;
	}

	public String getChange() {
		return change;
	}

	public void setChange(String change) {
		this.change = change;
	}

}
