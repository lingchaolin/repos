package model;
 // 车辆修理记录
public class RepairRecord {
	private Integer recordId; // 记录id
	private Integer bicycleId; //车辆id
	private String repairType; //修理类型
	private String repairPart;// 修理部位
	private String repairResult; // 修理结果
	private Double fee; // 修理费用
	private String repairer;// 修理人 
	private String repairDate;// 修理日期
	private String remark;// 备注
	private Integer userId;// 创建人
	private String  operatorTime;// 创建时间
	public RepairRecord(Integer recordId, Integer bicycleId, String repairType, String repairPart, String repairResult,
			Double fee, String repairer, String repairDate, String remark, Integer userId, String operatorTime) {
		super();
		this.recordId = recordId;
		this.bicycleId = bicycleId;
		this.repairType = repairType;
		this.repairPart = repairPart;
		this.repairResult = repairResult;
		this.fee = fee;
		this.repairer = repairer;
		this.repairDate = repairDate;
		this.remark = remark;
		this.userId = userId;
		this.operatorTime = operatorTime;
	}
	public RepairRecord() {
		super();
	}
	public Integer getRecordId() {
		return recordId;
	}
	public void setRecordId(Integer recordId) {
		this.recordId = recordId;
	}
	public Integer getBicycleId() {
		return bicycleId;
	}
	public void setBicycleId(Integer bicycleId) {
		this.bicycleId = bicycleId;
	}
	public String getRepairType() {
		return repairType;
	}
	public void setRepairType(String repairType) {
		this.repairType = repairType;
	}
	public String getRepairPart() {
		return repairPart;
	}
	public void setRepairPart(String repairPart) {
		this.repairPart = repairPart;
	}
	public String getRepairResult() {
		return repairResult;
	}
	public void setRepairResult(String repairResult) {
		this.repairResult = repairResult;
	}
	public Double getFee() {
		return fee;
	}
	public void setFee(Double fee) {
		this.fee = fee;
	}
	public String getRepairer() {
		return repairer;
	}
	public void setRepairer(String repairer) {
		this.repairer = repairer;
	}
	public String getRepairDate() {
		return repairDate;
	}
	public void setRepairDate(String repairDate) {
		this.repairDate = repairDate;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public String getOperatorTime() {
		return operatorTime;
	}
	public void setOperatorTime(String operatorTime) {
		this.operatorTime = operatorTime;
	}
	
}
